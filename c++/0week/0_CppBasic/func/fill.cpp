#include<iostream>
using namespace std;

int a[10];
int b[10][10];
int main(){
	fill(&a[0], &a[10], 100);
	
	for(int i = 0; i<10; i++){
		cout<<a[i]<<" ";
	}
	cout << endl;
	fill(&b[0][0], &b[9][10], 2);
	for(int i=0; i<10; i++){
		cout << i << " : ";
		for(int j=0; j<10; j++){
			cout << b[i][j] << " ";
		}
		cout << endl;
	}

	fill(a, a+10, 99);
	for(int i=0; i<10; i++){
		cout << a[i] << " ";
	}
	cout << endl;
	fill(&b[0][0], &b[0][0] + 9*10, 9);
	for(int i=0; i<10; i++){
		cout << i << " : ";
		for(int j=0; j<10; j++){
			cout << b[i][j] << " ";
		}
		cout << endl;
	}

	fill(a, a+10, 11);
	for(int i=0; i<10; i++){
		cout << a[i] << " ";
	}
	cout << endl;
	fill(&b[0][0], &b[0][0] + 10*10, 1);
	for(int i=0; i<10; i++){
		cout << i << " : ";
		for(int j=0; j<10; j++){
			cout << b[i][j] << " ";
		}
		cout << endl;
	}
	return 0;
}
